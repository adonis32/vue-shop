<template>
  <div
    v-scroll
    class="search-result-view"
  >
    <div class="search-result-view__content">
      <template v-if="isPlaylistsExists">
        <entity-header
          @click.native="goTo('search-playlist')"
          title="Playlists"
          small
        />
        <media-container>
          <media-object
            v-for="(playlist, index) in playlists.items"
            v-if="index < maxResults"
            :key="playlist.id"
            :id="playlist.id"
            :uri="playlist.uri"
            :coverImg="playlist.images"
            :name="playlist.name"
            :type="playlist.type"
          />
        </media-container>
      </template>

      <template v-if="isAlbumsExists">
        <entity-header
          @click.native="goTo('search-album')"
          title="Albums"
          small
        />
        <media-container>
          <media-object
            v-for="(album, index) in albums.items"
            v-if="index < maxResults"
            :key="album.id"
            :id="album.id"
            :uri="album.uri"
            :coverImg="album.images"
            :name="album.name"
            :artists="album.artists"
            :type="album.type"
          />
        </media-container>
      </template>

      <template v-if="isArtistsExists">
        <entity-header
          @click.native="goTo('search-artist')"
          title="Artists"
          small
        />
        <media-container>
          <media-object
            v-for="(artist, index) in artists.items"
            v-if="index < maxResults"
            :key="artist.id"
            :id="artist.id"
            :uri="artist.uri"
            :name="artist.name"
            :type="artist.type"
            :coverImg="artist.images"
          />
        </media-container>
      </template>
    </div>
  </div>
</template>

<script>
  import {mapState} from 'vuex'
  import router from '@/router'
  import EntityHeader from '@/components/EntityHeader'
  import MediaObject from '@/components/MediaObject'
  import MediaContainer from '@/components/MediaContainer'

  export default {
    name: 'search-result-view',

    components: {
      MediaObject,
      EntityHeader,
      MediaContainer
    },

    data() {
      return {
        maxResults: 12,
      }
    },

    computed: {
      ...mapState('search', [
        'query',
        'result',
        'isLoading',
        'error',
        'albums',
        'artists',
        'playlists',
      ]),

      isAlbumsExists() {
        return this.albums && this.albums.total > 0
      },

      isArtistsExists() {
        return this.artists && this.artists.total > 0
      },

      isPlaylistsExists() {
        return this.playlists && this.playlists.total > 0
      },
    },

    methods: {
      goTo(name) {
        router.push({name, params: {query: this.query}})
      }
    },
  }
</script>

<style scoped lang="sass">

